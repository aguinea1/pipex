#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*l;

	l = NULL;
	while (*s != '\0')
	{
		if (*s > 0)
		{
			if (*s == (char)c)
				l = (char *)s;
		}
		s++;
	}
	if ((char)c == '\0')
		return ((char *)s);
	return (l);
}
